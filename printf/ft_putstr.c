#include "ft_printf.h"

int	ft_putstr(char *s, int j)
{
	int	i;

	i = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		j += 6;
		return (j);
	}
	while (s[i])
	{
		j = ft_putchar(s[i], j);
		i++;
	}
	return (j);
}
